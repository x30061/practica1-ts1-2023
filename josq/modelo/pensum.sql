CREATE DATABASE pensum;
USE pensum;
CREATE TABLE cursos
(
    semestre INT NOT NULL,
	codigo INT,
	nombre VARCHAR(64) NOT NULL,
	PRIMARY KEY (codigo)
);
CREATE TABLE requisitos
(
	curso INT NOT NULL,
	requisito INT NOT NULL,
	FOREIGN KEY (curso) REFERENCES cursos(codigo),
	FOREIGN KEY (requisito) REFERENCES cursos(codigo),
	PRIMARY KEY (curso, requisito)
);
